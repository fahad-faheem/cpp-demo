#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 3;
    cout << "Relational Operators:" << endl;
    cout << "a == b: " << (a == b) << endl; // Equal to
    cout << "a != b: " << (a != b) << endl; // Not equal to
    cout << "a > b: " << (a > b) << endl;  // Greater than
    cout << "a < b: " << (a < b) << endl;  // Less than
    cout << "a >= b: " << (a >= b) << endl; // Greater than or equal to
    cout << "a <= b: " << (a <= b) << endl; // Less than or equal to
    return 0;
}

// Relational operators compare two values and determine the relationship between them, 
// such as whether they are equal, not equal, greater than, or less than. 
// These operators always return a boolean result: 
// true (if the condition is satisfied) or false (if it is not). 
// They are fundamental in making decisions in programs.

