#include <iostream>
using namespace std;

int main() {
    bool x = true, y = false;
    cout << "Logical Operators:" << endl;
    cout << "x && y: " << (x && y) << endl; // Logical AND
    cout << "x || y: " << (x || y) << endl; // Logical OR
    cout << "!x: " << (!x) << endl;         // Logical NOT
    return 0;
}


// Logical operators are used to combine multiple conditions or logical expressions. 
// They include AND (&&), OR (||), and NOT (!). Logical operators are essential in decision-making,
// as they help evaluate whether conditions are true or false in a program, 
// especially in complex scenarios involving multiple criteria


